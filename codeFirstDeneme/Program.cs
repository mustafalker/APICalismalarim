using codeFirstDeneme.Repositories;
using Microsoft.EntityFrameworkCore;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

//Buraya Baðlantý Dizesini Girdik Aþaðýya (Repository Context Denilen Kýsým Bizim Ýsimlendirmemizdir.).
builder.Services.AddDbContext<RepositoryContext>(options => options.UseSqlServer
(builder.Configuration.GetConnectionString("sqlConnection")));//bunu verdiðimiz zaman IOC ye tanýmý yapmýþ oluyoruz : Inversion Of Control 

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
